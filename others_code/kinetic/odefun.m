function [dy,rF,rR] = odefun(t,y,kf,kr,RT)
rF = ...
[(kf(1)*y(3))/20000;...
 kf(2)*y(2)*y(5);...
 kf(3)*y(7);...
 (21*kf(4)*y(8))/1000;...
 kf(5)*y(2)*y(6);...
 kf(6)*y(3)*y(4);...
 kf(7)*y(1)];

rR = ...
[kr(1)*y(2);...
 kr(2)*y(3)*y(7);...
 (kr(3)*y(8))/25000;...
 kr(4)*y(6);...
 kr(5)*y(3)*y(4);...
 kr(6)*y(1)*y(5);...
 (kr(7)*y(3))/25000];

r = rF - rR;

dy = ...
[r(6) - r(7);...
 r(1) - r(2) - r(5);...
 r(2) - r(1) + r(5) - r(6) + r(7);...
 r(5) - r(6);...
 r(6) - r(2);...
 r(4) - r(5);...
 r(2) - r(3);...
 r(3) - r(4)];